package br.com.exerciciosJava;

/*Calcule e apresente a média entre os números 8, 9 e 7.*/

public class ex04 {
	public static void main(String[]args) {
		int oito, nove, sete, media;
		
		oito = 8;
		nove = 9;
		sete = 7;
		media = (oito + nove + sete) / 3;
		
		System.out.println("A média é " + media);
	}
}
